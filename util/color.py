"""This file defines Colors in BGR Format(BLUE, GREEN, RED)."""

from typing import Tuple


BGR = Tuple[int, int, int]

BLACK:      BGR = (0, 0, 0)
BLUE:       BGR = (255, 0, 0)
CHOCOLATE:  BGR = (30, 105, 210)
CYAN:       BGR = (255, 255, 0)
GOLDEN:     BGR = (32, 218, 165)
GARY:       BGR = (155, 155, 155)
GREEN:      BGR = (0, 255, 0)
LIGHT_BLUE: BGR = (255, 9, 2)
MAGENTA:    BGR = (255, 0, 255)
ORANGE:     BGR = (0, 69, 255)
PURPLE:     BGR = (128, 0, 128)
PINK:       BGR = (147, 20, 255)
RED:        BGR = (0, 0, 255)
WHITE:      BGR = (255, 255, 255)
YELLOW:     BGR = (0, 255, 255)
